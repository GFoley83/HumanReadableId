        public static bool WordIsInWordList(string word, WordType wordType)
        {
            switch (wordType)
            {
                case WordType.Adjective:
                    return Adjectives.Contains(word, StringComparer.OrdinalIgnoreCase);
                case WordType.Verb:
                    return Verbs.Contains(word, StringComparer.OrdinalIgnoreCase);
                case WordType.Animal:
                    return Animals.Contains(word, StringComparer.OrdinalIgnoreCase);
            }

            return false;
        }

